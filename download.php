function sendContact() {
	var valid;	
	valid = validateContact();
	if(valid) {
		jQuery.ajax({
		url: "contact_mail3.php",
		data:'w3lName='+$("#w3lName").val()+'&w3lSender='+$("#w3lSender").val()+'&w3lnum='+$("#w3lnum").val()+'&w3lMessage='+$("#w3lMessage").val(),
		type: "POST",
		success:function(data){
		$("#mail-status").html(data);
		},
		error:function (){}
		});
		$("input[type=text], input[type=email], textarea").val("");
	}
}
function validateContact() {
	var valid = true;	
	$(".demoInputBox").css('background-color','');
	$(".info").html('');
	
	if(!$("#w3lName").val()) {
		$("#w3lName-info").html("(required)");
		$("#w3lName").css('background-color','#FFFFDF');
		valid = false;
	}
	if(!$("#w3lMessage").val()) {
		$("#w3lMessage-info").html("(required)");
		$("#w3lMessage").css('background-color','#FFFFDF');
		valid = false;
	}
	if(!$("#w3lSender").val().match(/^([\w-\.]+@([\w-]+\.)+[\w-]{2,4})?$/)) {
		$("#w3lSender-info").html("(invalid)");
		$("#w3lSender").css('background-color','#FFFFDF');
		valid = false;
	}
	if(!$("#w3lnum").val().match(/^\d{10}$/)) {
		$("#w3lnum-info").html("(invalid)");
		$("#w3lnum").css('background-color','#FFFFDF');
		valid = false;
	}
	
	
	return valid;
}
